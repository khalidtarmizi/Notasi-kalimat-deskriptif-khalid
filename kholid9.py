# suhu dalam celcius = 42
# suhu dalam fahrenheit = 107.6
# suhu dalam kelvin = 315.15
# suhu dalam reamur = 33.6

# suhu dalam celcius
celcius = 42

# rumus koversi suhu
farenheit = (celcius * 9/5) + 32
kelvin = celcius + 273.15
reamur = celcius * 4/5

# cetak nilai suhu
print("suhu dalam fahrenheit:", farenheit )
print("suhu dalam kelvin:", kelvin)
print("suhu dalam reamur:", reamur)